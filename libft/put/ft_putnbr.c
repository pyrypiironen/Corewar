#include "../libft.h"

void	ft_putnbr(int n)
{
	int		i;
	int		arr[20];
	long	k;

	i = 0;
	k = n;
	if (k < 0)
	{
		k = k * -1;
		ft_putchar('-');
	}
	arr[i] = k % 10 + '0';
	i++;
	while (k / 10 > 0)
	{
		k = k / 10;
		arr[i] = (k % 10 + '0');
		i++;
	}
	i--;
	while (i >= 0)
	{
		ft_putchar((char)arr[i]);
		i--;
	}
}
