#include "../libft.h"

char	*ft_strdup(const char *s1)
{
	char		*dup;
	size_t		len;

	if (s1 == NULL)
		return (NULL);
	len = ft_strlen(s1);
	dup = (char *)ft_memalloc(sizeof(char) * (len + 1));
	if (dup == NULL)
		return (NULL);
	len = 0;
	while (s1[len])
	{
		dup[len] = s1[len];
		len++;
	}
	dup[len] = '\0';
	return (dup);
}
